package com.bonc.model.hostmo;

public class HostmoBean {
	public String host;
	public String root_used;
	public String data_used;
	public String cpu_used;
	public String mem_used;
	public String getHost() {
		return host;
	}
	public void setHost(String host) {
		this.host = host;
	}
	public String getRoot_used() {
		return root_used;
	}
	public void setRoot_used(String root_used) {
		this.root_used = root_used;
	}
	public String getData_used() {
		return data_used;
	}
	public void setData_used(String data_used) {
		this.data_used = data_used;
	}
	public String getCpu_used() {
		return cpu_used;
	}
	public void setCpu_used(String cpu_used) {
		this.cpu_used = cpu_used;
	}
	public String getMem_used() {
		return mem_used;
	}
	public void setMem_used(String mem_used) {
		this.mem_used = mem_used;
	}
	

}
