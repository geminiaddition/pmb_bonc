package com.bonc.model.jiagong;

public class WorkBean {

	public String prov_desc; 
	public int totals;
	public int succnum;
	public int failnum;
	public int procingNum;
	public String getProv_desc() {
		return prov_desc;
	}
	public void setProv_desc(String prov_desc) {
		this.prov_desc = prov_desc;
	}
	public int getTotals() {
		return totals;
	}
	public void setTotals(int totals) {
		this.totals = totals;
	}
	public int getSuccnum() {
		return succnum;
	}
	public void setSuccnum(int succnum) {
		this.succnum = succnum;
	}
	public int getFailnum() {
		return failnum;
	}
	public void setFailnum(int failnum) {
		this.failnum = failnum;
	}
	public int getProcingNum() {
		return procingNum;
	}
	public void setProcingNum(int procingNum) {
		this.procingNum = procingNum;
	}
	
	
}
