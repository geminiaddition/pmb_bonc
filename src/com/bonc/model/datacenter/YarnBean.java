package com.bonc.model.datacenter;

public class YarnBean {
	public String scheduler_id;
	public String userd;
	public String absolute_capacity;
	public String getScheduler_id() {
		return scheduler_id;
	}
	public void setScheduler_id(String scheduler_id) {
		this.scheduler_id = scheduler_id;
	}
	public String getUserd() {
		return userd;
	}
	public void setUserd(String userd) {
		this.userd = userd;
	}
	public String getAbsolute_capacity() {
		return absolute_capacity;
	}
	public void setAbsolute_capacity(String absolute_capacity) {
		this.absolute_capacity = absolute_capacity;
	}
	

}
